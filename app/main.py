def battle(knights_config: dict) -> dict:
    # BATTLE PREPARATIONS:

    # lancelot
    lancelot = Knights(*knights_config["lancelot"].values())
    lancelot.arm_himself()
    lancelot.putting_on()
    lancelot.application()

    # arthur
    arthur = Knights(*knights_config["arthur"].values())
    arthur.arm_himself()
    arthur.putting_on()
    arthur.application()

    # mordred
    mordred = Knights(*knights_config["mordred"].values())
    mordred.arm_himself()
    mordred.putting_on()
    mordred.application()

    # red_knight
    red_knight = Knights(*knights_config["red_knight"].values())
    red_knight.arm_himself()
    red_knight.putting_on()
    red_knight.application()

    # -------------------------------------------------------------------------------
    # BATTLE:

    # 1 Lancelot vs Mordred:
    lancelot.battle(mordred)

    # 2 Arthur vs Red Knight:
    arthur.battle(red_knight)

    # Return battle results:
    return {
        lancelot.name: lancelot.hp,
        arthur.name: arthur.hp,
        mordred.name: mordred.hp,
        red_knight.name: red_knight.hp,
    }


